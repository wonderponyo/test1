package net.inf;


public class Loginf {

  private long id;
  private String optime;
  private String opman;
  private String optype;
  private String opthing;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getOptime() {
    return optime;
  }

  public void setOptime(String optime) {
    this.optime = optime;
  }


  public String getOpman() {
    return opman;
  }

  public void setOpman(String opman) {
    this.opman = opman;
  }


  public String getOptype() {
    return optype;
  }

  public void setOptype(String optype) {
    this.optype = optype;
  }


  public String getOpthing() {
    return opthing;
  }

  public void setOpthing(String opthing) {
    this.opthing = opthing;
  }

}
